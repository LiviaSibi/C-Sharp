using System;

namespace Sequencia100
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.Clear();
            System.Console.WriteLine("===Sequência de 0 a 100===");
            System.Console.WriteLine();

            for(int i=1; i<=100; i++){
                System.Console.Write(i + " ");
            }
            System.Console.WriteLine();
        }
    }
}
