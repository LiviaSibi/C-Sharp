using System;

namespace Sequencia120a300
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.Clear();
            System.Console.WriteLine("===Sequência de 120 a 300===");
            System.Console.WriteLine();

            for(int i=120; i<=300; i++){
                System.Console.Write(i + " ");
            }
            System.Console.WriteLine();
        }
    }
}
